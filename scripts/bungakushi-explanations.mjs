export function isNamedAuthor(author) {
  if (!author) return false;
  if (/編者|編纂者|作者不詳|不明|^不詳$/.test(author)) return false;
  if (/^(文官|神祇官|平家)/.test(author)) return false;
  if (/ら$|ほか$/.test(author)) return false;
  return true;
}

export function authorQuestionLabel(work) {
  if (/万葉集/.test(work)) return '編者';
  if (/百人一首/.test(work)) return '撰者';
  if (/歌集|和歌集|風土記|記紀|懐風藻/.test(work) && !/物語/.test(work)) {
    return '撰者';
  }
  return '作者';
}

