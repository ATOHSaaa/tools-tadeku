import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { KINDAI_AUTHOR_PAIRS } from './kindai-author-pairs.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outPath = path.join(__dirname, '../shared/kindai-author-quiz-data.js');

const GRADES = [
  { min: 100, name: '文学博士', blurb: '完璧です。作者と作品の対応を完璧に把握しています。' },
  { min: 80, name: '文学部首席', blurb: 'ほぼ満点。細部まで押さえています。' },
  { min: 60, name: '文学徒見習い', blurb: 'しっかり基礎が身についています。' },
  { min: 40, name: '読書家', blurb: '要点は掴めています。復習で伸びしろ大。' },
  { min: 0, name: '読書家見習い', blurb: '解説を読みながら、もう一度挑戦してみましょう。' },
];

const ROUND_SIZE = 10;
const QUESTION_COUNT = 100;

function slug(text) {
  return String(text).replace(/[^\w\u3040-\u30ff\u4e00-\u9faf]+/g, '-').slice(0, 40);
}

function shuffle(list) {
  const copy = list.slice();
  for (let i = copy.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}

function pickOthers(pool, value, count) {
  const others = [...new Set(pool.filter((item) => item !== value))];
  return shuffle(others).slice(0, count);
}

function makeQuestion(id, work, author, body, authorNames) {
  const wrong = pickOthers(authorNames, author, 3);
  if (wrong.length < 3) return null;
  const choices = shuffle([author, ...wrong]);
  return {
    id,
    text: `『${work}』の作者は誰？`,
    choices,
    correct: choices.indexOf(author),
    explanation: `正解は${author}。${body}`,
  };
}

function buildQuestions() {
  if (KINDAI_AUTHOR_PAIRS.length < QUESTION_COUNT) {
    throw new Error(`ペアが${KINDAI_AUTHOR_PAIRS.length}件しかありません（${QUESTION_COUNT}件必要）`);
  }

  const seen = new Set();
  const pairs = [];
  for (const pair of KINDAI_AUTHOR_PAIRS) {
    if (seen.has(pair.work)) {
      throw new Error(`作品名が重複しています: ${pair.work}`);
    }
    seen.add(pair.work);
    pairs.push(pair);
    if (pairs.length >= QUESTION_COUNT) break;
  }

  if (pairs.length < QUESTION_COUNT) {
    throw new Error(`ユニークな作品が${pairs.length}件しかありません（${QUESTION_COUNT}件必要）`);
  }

  const authorNames = [...new Set(pairs.map((p) => p.author))];
  return pairs.map((p) => makeQuestion(
    `ka-${slug(p.work)}`,
    p.work,
    p.author,
    p.body,
    authorNames,
  )).filter(Boolean);
}

const QUESTIONS = buildQuestions();

if (QUESTIONS.length !== QUESTION_COUNT) {
  throw new Error(`問題数が${QUESTIONS.length}問です（${QUESTION_COUNT}問必要）`);
}

const output = `/* generated by scripts/build-kindai-author-quiz-data.mjs — do not edit by hand */
(function () {
  const GRADES = ${JSON.stringify(GRADES, null, 2)};

  const ROUND_SIZE = ${ROUND_SIZE};

  const QUESTIONS = ${JSON.stringify(QUESTIONS, null, 2)};

  window.KindaiAuthorQuizData = {
    GRADES,
    ROUND_SIZE,
    QUESTIONS,
    QUESTION_COUNT: ${QUESTION_COUNT},
  };
})();
`;

fs.writeFileSync(outPath, output, 'utf8');
console.log(`Wrote ${QUESTIONS.length} questions to ${outPath}`);
